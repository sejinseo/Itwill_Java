package edu.java.conditional02;

public class ConditionalMain02 {

	public static void main(String[] args) {
		// if - else 문장
		int score = 59;
		
		if (score >= 60) {  // 만약에 score가 60 이상이면,
			System.out.println("합격");
		} else {  // 그렇지 않으면,
			System.out.println("불합격");
		}  // if - else 구문 끝

		System.out.println("=== end ===");
	}

}