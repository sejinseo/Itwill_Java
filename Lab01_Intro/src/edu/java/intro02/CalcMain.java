package edu.java.intro02;

public class CalcMain {  // 클래스 시작
	// main 메서드(기능)
	public static void main(String[] args) {  // main 메서드 시작
		System.out.println(100 + 200);  // 100 + 200 계산 결과를 출력
		System.out.println(100 * 200);  // 100 x 200 계산 결과를 출력
		System.out.println(10 / 2);  // 10 / 2 계산 결과를 출력
		
		// 위 결과와 비교
		System.out.println("100 + 200");  // 문자열(string)을 출력
		// 문자열(string): 문자들의 집합(sequence). 큰따옴표("")로 묶음.
		// 문자(character): 문자 (1개). 작은따옴표('')로 묶음.
		System.out.println('a');  // a 문자 (1개)를 콘솔에 출력.
		System.out.println("a");  // 문자 1개로 이루어진 문자열을 콘솔에 출력.
		
	}  // main 메서드 끝
	
}  // 클래스 끝